.MODEL SMALL
.386
.DATA
matrixa DB 02H,03H,05H,06H,08H,04H,09H,0A0H,08H
matrixb DB 07H,05H,01H,02H,50H,90H,03H,04H,0AAH
matrixc DB 9 DUP(0)
matrixd DB ' $'

.CODE
.STARTUP

MOV CL,0

P1:
XOR BX,BX
MOV BL,CL
MOV AL,matrixa[BX]
MOV DL,matrixb[BX]
ADD AL,DL
MOV matrixc[BX],AL
INC CL
CMP CL,9
JNZ P1
MOV DX,0AH
MOV AH,02H
INT 21H
MOV CL,0
MOV CH,0
PP:XOR BX,BX
MOV BL,CL
MOV AL,matrixc[BX]
MOV DL,AL
ROL DL,4
AND DL,0FH
ADD DL,30H
CMP DL,'9'
JBE F1
ADD DL,7H
F1:
MOV AH,02H
INT 21H
MOV AL,matrixc[BX]
AND AL,0FH
MOV DL,AL
ADD DL,30H
CMP DL,'9'
JBE F2
ADD DL,7
HLT
F2:
MOV AH,02H
INT 21H
MOV DX,OFFSET matrixd
MOV AH,09H
INT 21H
INC CH
CMP CH,3
JNZ L1
MOV DX,0AH
MOV AH,02H
INT 21H
MOV CH,0

HLT

L1:
INC CL
CMP CL,9
JNZ PP
HLT
MOV AH,4CH
INT 21H
END