DATA SEGMENT
USERNAME DB 'rupak123'
LEN EQU ($-USERNAME)
MSG1 DB 10,13,'ENTER YOUR USERNAME: $'
MSG2 DB 10,13,'WELCOME TO Rupaks Code Cafe!$'
MSG3 DB 10,13,'INCORRECT USERNAME!$'

PASSWORD DB 'rupak'
LEN2 EQU ($-PASSWORD)
MSG4 DB 10,13,'ENTER YOUR PASSWORD: $'

MSG5 DB 10,13,'INCORRECT PASSWORD!$'
MSG6 DB 10,13,'CORRECT USERNAME!$'

NEW DB 10,13,'$'
INST DB 10 DUP(0)

NEW2 DB 10,13,'$'
INST2 DB 10 DUP(0)
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:
MOV AX,DATA
MOV DS,AX
LEA DX,MSG1
MOV AH,09H
INT 21H
MOV SI,00
UP1:
MOV AH,08H
INT 21H
CMP AL,0DH
JE DOWN
MOV [INST+SI],AL
MOV DL,'*'
MOV AH,02H
INT 21H
INC SI
JMP UP1
DOWN:
MOV BX,00
MOV CX,LEN
CHECK:
MOV AL,[INST+BX]
MOV DL,[USERNAME+BX]
CMP AL,DL
JNE FAIL
INC BX
LOOP CHECK

LEA DX,MSG6
MOV AH,09H
INT 21H
JMP CHECK2
FAIL:
LEA DX,MSG3
MOV AH,009H
INT 21H

CHECK2:
LEA DX,MSG4
MOV AH,09H
INT 21H
MOV SI,00
UP2:
MOV AH,08H
INT 21H
CMP AL,0DH
JE DOWN2
MOV [INST2+SI],AL
MOV DL,'*'
MOV AH,02H
INT 21H
INC SI
JMP UP2
DOWN2:
MOV BX,00
MOV CX,LEN
CHECK3:
MOV AL,[INST2+BX]
MOV DL,[PASSWORD+BX]
CMP AL,DL
JNE FAIL
INC BX
LOOP CHECK3

LEA DX,MSG2
MOV AH,09H
INT 21H
JMP FINISH
FAIL:
LEA DX,MSG5
MOV AH,009H
INT 21H
FINISH:
INT 3
mov ah,4ch
int 21h
CODE ENDS
END START
END